package com.turingtraining.pos.dao.model;

/**
 *
 * @author DELL
 */
public class UserType {

    private Long id;
    private String name;

    public UserType(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }

}
